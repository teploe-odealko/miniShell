#include <stddef.h>

void	*ft_memset(void *b, int c, size_t len)
{
	size_t			i;
	unsigned char	*d;

	i = 0;
	d = b;
	while (len != 0 && i < len)
	{
		*d++ = (unsigned char)c;
		i++;
	}
	return (b);
}
